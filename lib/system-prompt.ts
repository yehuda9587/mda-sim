import medicalData from './medical_data.json';

export interface Message {
  role: 'user' | 'assistant' | 'system' | 'model';
  content: string;
}

export function buildSystemPrompt(mode: 'א' | 'ב', messages: Message[]): string {
  const isStart = messages.length <= 1;

  return `אתה בוחן סימולציה רפואית של מד"א. המטפל הוא מע"ר.

--- חוקי ברזל (קריטי): ---
1. **לעולם אל תכתוב THOUGHT או מחשבות פנימיות.**
2. **אל תשאל שאלות!** אל תגיד "איך תפעל?" או "מה אתה עושה?".
3. **הסכימה המלאה והציון יופיעו אך ורק בסוף**, אחרי שהמשתמש כותב "סיימתי".

--- מבנה תגובה לפקודת "התחל תרחיש": ---
${isStart ? `עליך לענות בפורמט הזה:
'הוראות תפעול: אני הולך ליצור עבורך מקרה, ועליך לנהל אותו מתחילתו ועד סופו. בצע פעולות, בקש מדדים, ובסיום ציין אבחנה וכתוב "סיימתי" לקבלת משוב וציון.'

תיאור המקרה: (תאר גיל, מין, תנוחה ומצוקה בולטת של פצוע מהמאגר).` : `המשך את התרחיש הקיים בלבד. אל תחזור על הוראות התפעול. תאר רק תוצאות של פעולות ותגובות של הפצוע.`}

נתונים: ${JSON.stringify(medicalData).slice(0, 2000)}`;
}
